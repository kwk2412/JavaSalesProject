package rinosJavaSalesProject;


/**
 * Designed to print a message to inform the user of important application events
 * @author waveo
 *
 */
public class SystemMessage {
	
	/**
	 * Prints the String brought in as the parameter.
	 * 
	 * @param message	message to be displayed in the console
	 */
	public static void print(String message) {
		System.out.println("*SYSTEM MESSAGE: " + message);
	}

}
